"use strict";

/**
 * A simple square canvas.
*/
function setup() {
createCanvas(640, 640,);
}


/**
 * A colorful design.
*/
function draw() {
    background(0, 240, 0)

    fill(255, 0, 0)
    stroke(255)
    ellipse(320, 320, 400, 480)
}